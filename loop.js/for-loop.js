for(let num = 0; num < 10; num++){
    console.log(num);
}


// odd 
for(let i = 1; i< 20; i=i+2){
    console.log(i);
}


// even 
for (let j=0; j<20; j=j+2){
    console.log(j);
}



// incremental loop
for(let x = 0; x <= 10; x++){
    console.log(x);
}



// decremental loop
for(let z = 10; z >= 0; z--){
    console.log(z);
}